export function pickFallbackQuestion({ theme, difficulty, recentQuestions = [] }) {
  const fallbackTheme = FALLBACK_QUESTION_BANK[theme] ? theme : "Tema livre";
  const themeBank = FALLBACK_QUESTION_BANK[fallbackTheme] || FALLBACK_QUESTION_BANK["Tema livre"];
  const candidates = Array.isArray(themeBank?.[difficulty]) ? themeBank[difficulty] : [];
  const recentSignatures = new Set(recentQuestions.map(createSignature));
  const freshCandidate = candidates.find((candidate) => !recentSignatures.has(createSignature(candidate.question)));
  const selected = freshCandidate || candidates[0] || FALLBACK_QUESTION_BANK["Tema livre"][difficulty]?.[0];

  return {
    answer: selected.answer,
    difficulty,
    explanation: selected.explanation,
    generatedAt: new Date().toISOString(),
    hint: selected.hint,
    provider: "local",
    question: selected.question,
    source: "fallback",
    theme: fallbackTheme,
  };
}

function createSignature(value) {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}
